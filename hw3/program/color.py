def get_color(num):
    if num == 1:
        return "red"
    if num == 2:
        return "orange"
    if num == 3:
        return "yellow"
    if num == 4:
        return "green"
    if num == 5:
        return "cyan"
    if num == 6:
        return "blue"
    if num == 7:
        return "purple"
    return "undefined"
